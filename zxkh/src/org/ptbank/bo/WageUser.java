package org.ptbank.bo;

public class WageUser {
	private String sfzhm;
	private String name;
	private String dwmc;
	private String bmmc;
	private String passwd;
	private String lst_log_dt;
	private String lst_log_tm;
	private String flag;
	public String getSfzhm() {
		return sfzhm;
	}
	public void setSfzhm(String sfzhm) {
		this.sfzhm = sfzhm;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDwmc() {
		return dwmc;
	}
	public void setDwmc(String dwmc) {
		this.dwmc = dwmc;
	}
	public String getBmmc() {
		return bmmc;
	}
	public void setBmmc(String bmmc) {
		this.bmmc = bmmc;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getLst_log_dt() {
		return lst_log_dt;
	}
	public void setLst_log_dt(String lst_log_dt) {
		this.lst_log_dt = lst_log_dt;
	}
	public String getLst_log_tm() {
		return lst_log_tm;
	}
	public void setLst_log_tm(String lst_log_tm) {
		this.lst_log_tm = lst_log_tm;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
}
